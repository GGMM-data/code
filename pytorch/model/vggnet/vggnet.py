import torch 
import torch.nn as nn
import torch.nn.functional as F


class VGG(nn.Module):
    def __init__(self, features, num_classes=1000, init_weigths=True):
        super(VGG, self).__init__()
        self.features = features
        self.classifier = nn.Sequential( 
            nn.Linear(7 * 7 * 512, 4096),
            nn.ReLU(inplace=True),
            nn.Dropout(),
            nn.Linear(4096, 4096),
            nn.ReLU(inplace=True),
            nn.Dropout(),
            nn.Linear(4096, num_classes)
        )

    def forward(self, x):
        x = self.features(x)
        x = x.view(x.size(0), -1)
        x = self.classifier(x)
        return x


