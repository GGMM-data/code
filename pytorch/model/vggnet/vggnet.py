def make_layers(conf):
    layers = []
    in_channels = 3
    for l in conf:
        if l == 'M':
            layers.append(nn.MaxPool2d(2)) 
        else:
            layers.append(nn.Conv2d(in_channels, l, kernel_size=3, padding=1))
            in_channels = l

    return nn.Sequential(*layers)

